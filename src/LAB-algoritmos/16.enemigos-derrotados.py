# Calcular el porcentaje de enemigos derrotados: Escribe un algoritmo que solicite el número total 
# de enemigos y el número de enemigos derrotados, y calcule el porcentaje de enemigos derrotados.

total_enemigos = 40
enemigos_derrotados = 25

porcentaje = (enemigos_derrotados / total_enemigos) * 100
print("El porcentaje de enemigos derrotados es de", porcentaje, "%")